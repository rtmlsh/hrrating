def _random_date(days_back=400):
    return timezone.now() - timedelta(days=random.randint(1, days_back))


def _weights_for_target(target):
    """
    Возвращает веса {5,4,3,2,1} так, чтобы математическое ожидание
    совпадало с target (0–5).  Линейная интерполяция между опорными точками.
    """
    anchors = {
        5.0: {5: 80, 4: 15, 3: 4,  2: 1,  1: 0},
        4.5: {5: 55, 4: 35, 3: 8,  2: 2,  1: 0},
        4.0: {5: 30, 4: 45, 3: 18, 2: 5,  1: 2},
        3.5: {5: 10, 4: 30, 3: 40, 2: 15, 1: 5},
        3.0: {5: 5,  4: 15, 3: 45, 2: 25, 1: 10},
        2.5: {5: 2,  4: 8,  3: 30, 2: 40, 1: 20},
    }
    keys = sorted(anchors.keys(), reverse=True)
    t = max(2.5, min(5.0, target))
    for i, k in enumerate(keys):
        if t >= k or i == len(keys) - 1:
            return anchors[k]
        lo, hi = keys[i + 1], k
        frac = (t - lo) / (hi - lo)
        result = {}
        for r in range(1, 6):
            result[r] = anchors[lo][r] + frac * (anchors[hi][r] - anchors[lo][r])
        return result


def _generate_reviews_for(agency):
    count = random.randint(10, 15)

    crits = [agency.criteria_quality, agency.criteria_speed,
             agency.criteria_price, agency.criteria_support]
    filled = [c for c in crits if c > 0]
    target = sum(filled) / len(filled) if filled else 4.0

    weights = _weights_for_target(target)
    ratings = random.choices(
        list(weights.keys()),
        weights=list(weights.values()),
        k=count,
    )

    reviews = []
    for rating in ratings:
        if rating == 5:
            author = random.choice(AUTHORS_5)
            text = random.choice(TEXTS_5)
        elif rating == 4:
            author = random.choice(AUTHORS_4)
            text = random.choice(TEXTS_4)
        elif rating == 3:
            author = random.choice(AUTHORS_3)
            text = random.choice(TEXTS_3)
        else:
            author = random.choice(AUTHORS_LOW)
            text = random.choice(TEXTS_2)

        source = random.choice(SOURCES)
        reviews.append(Review(
            agency=agency,
            rating=rating,
            author=author,
            text=text,
            source=source,
            is_approved=True,
            created_at=_random_date(),
        ))

    Review.objects.bulk_create(reviews)
    return len(reviews)


class Command(BaseCommand):
    help = "Генерирует 10–15 реалистичных отзывов для каждого агентства"

    def add_arguments(self, parser):
        parser.add_argument(
            '--clear', action='store_true',
            help='Удалить существующие отзывы перед генерацией'
        )

    def handle(self, *args, **options):
        agencies = Agency.objects.all()
        total = 0

        if options['clear']:
            deleted, _ = Review.objects.all().delete()
            self.stdout.write(self.style.WARNING(f"Удалено {deleted} отзывов"))

        for agency in agencies:
            n = _generate_reviews_for(agency)
            total += n
            self.stdout.write(f"  {agency.name}: +{n} отзывов")

        self.stdout.write(self.style.SUCCESS(f"\nГотово. Добавлено {total} отзывов для {agencies.count()} агентств."))
