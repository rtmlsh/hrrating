class Command(BaseCommand):
    help = "Add HR agencies and city pages for 11 Russian cities"

    def handle(self, *args, **options):
        cat_map = {c.slug: c for c in Category.objects.all()}

        for city_data in CITIES_DATA:
            self.stdout.write(f"\n→ {city_data['name']}")

            # Create or update CityPage
            cp, created = CityPage.objects.update_or_create(
                slug=city_data["slug"],
                defaults={
                    "name": city_data["name"],
                    "name_genitive": city_data["name_genitive"],
                    "city_filter": city_data["city_filter"],
                    "is_published": True,
                    "meta_title": city_data["meta_title"],
                    "meta_description": city_data["meta_description"],
                    "meta_keywords": city_data["meta_keywords"],
                    "h1": city_data["h1"],
                    "hero_subtitle": city_data["hero_subtitle"],
                    "agencies_heading": city_data["agencies_heading"],
                    "stat1_value": "50+",
                    "stat1_label": "проверенных\nагентств",
                    "stat2_value": "4",
                    "stat2_label": "критерия\nоценки",
                    "stat3_value": "12",
                    "stat3_label": "месяцев\nисследования",
                    "stat4_value": "0₽",
                    "stat4_label": "платных\nразмещений",
                },
            )
            action = "создан" if created else "обновлён"
            self.stdout.write(f"  CityPage {action}: {cp.name}")

            # Update CityLink
            CityLink.objects.filter(name=city_data["name"]).update(
                url=f"/{city_data['slug']}/",
                is_active=True,
            )

            # Create agencies
            for ag_data in city_data["agencies"]:
                # Skip if already exists
                if Agency.objects.filter(name=ag_data["name"], city=city_data["city_filter"]).exists():
                    self.stdout.write(f"  [skip] {ag_data['name']} уже существует")
                    continue

                agency = Agency.objects.create(
                    name=ag_data["name"],
                    description=ag_data["description"],
                    city=city_data["city_filter"],
                    address=ag_data["address"],
                    phone=ag_data["phone"],
                    website=ag_data.get("website", ""),
                    founded_year=ag_data.get("founded_year"),
                    is_verified=ag_data.get("is_verified", False),
                    specializations=ag_data["specializations"],
                    criteria_quality=ag_data["criteria_quality"],
                    criteria_speed=ag_data["criteria_speed"],
                    criteria_price=ag_data["criteria_price"],
                    criteria_support=ag_data["criteria_support"],
                    speed_score=ag_data["speed_score"],
                )

                # Set categories
                for slug in ag_data.get("categories", []):
                    if slug in cat_map:
                        agency.categories.add(cat_map[slug])

                # Create reviews
                for rating, text, author in ag_data.get("reviews", []):
                    Review.objects.create(
                        agency=agency,
                        rating=rating,
                        text=text,
                        author=author,
                    )

                self.stdout.write(
                    f"  + {agency.name} | {len(ag_data['reviews'])} отзывов"
                )

        self.stdout.write(self.style.SUCCESS("\n✓ Готово!"))
